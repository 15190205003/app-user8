package com.hiwoo.model;


public class DataAuth {

    private Integer id;

    private Integer userId;

    private Integer isStoreData;

    private Integer expireTime;

    private Integer userCount;

    private Integer dataCount;

    private Integer msgCount;

    private Integer webcamCount;

    private Integer isLogoAndName;

    private Integer dataTotalCount;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getIsStoreData() {
        return isStoreData;
    }

    public void setIsStoreData(Integer isStoreData) {
        this.isStoreData = isStoreData;
    }

    public Integer getExpireTime() {
        return expireTime;
    }

    public void setExpireTime(Integer expireTime) {
        this.expireTime = expireTime;
    }

    public Integer getUserCount() {
        return userCount;
    }

    public void setUserCount(Integer userCount) {
        this.userCount = userCount;
    }

    public Integer getDataCount() {
        return dataCount;
    }

    public void setDataCount(Integer dataCount) {
        this.dataCount = dataCount;
    }

    public Integer getMsgCount() {
        return msgCount;
    }

    public void setMsgCount(Integer msgCount) {
        this.msgCount = msgCount;
    }

    public Integer getWebcamCount() {
        return webcamCount;
    }

    public void setWebcamCount(Integer webcamCount) {
        this.webcamCount = webcamCount;
    }

    public Integer getIsLogoAndName() {
        return isLogoAndName;
    }

    public void setIsLogoAndName(Integer isLogoAndName) {
        this.isLogoAndName = isLogoAndName;
    }

    public Integer getDataTotalCount() {
        return dataTotalCount;
    }

    public void setDataTotalCount(Integer dataTotalCount) {
        this.dataTotalCount = dataTotalCount;
    }
}
